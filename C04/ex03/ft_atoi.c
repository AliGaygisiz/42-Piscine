int	ft_atoi(char *str)
{
	unsigned int	i;
	int				s;
	int				nbr;

	i = 0;
	s = 1;
	nbr = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = s * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10;
		nbr = nbr + str[i] - 48;
		i++;
	}
	return (nbr * s);
}
