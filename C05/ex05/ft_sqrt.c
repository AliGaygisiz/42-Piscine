int	ft_sqrt(int nb)
{
	long	i;
	long	nbr;

	i = 1;
	nbr = nb;
	while (i * i <= nbr)
	{
		if (i * i < nbr)
		{
			i++;
		}
		else if (i * i == nbr)
		{
			return (i);
		}
	}
	return (0);
}
