int	ft_str_is_printable(char *str)
{
	int		i;
	int		r;

	i = 0;
	r = 1;
	while (str[i] != '\0')
	{
		if (str[i] < ' ' || str[i] > '~')
		{
			r = 0;
		}
		i++;
	}
	return (r);
}
